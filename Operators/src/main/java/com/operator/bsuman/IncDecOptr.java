package com.operator.bsuman;

public class IncDecOptr {
	public static void main(String[] args) {
		int x=10;
	     	x++;
	     System.out.println(x);
	     
	     char ch='a';
	     ch++;
	     System.out.println(ch);
	     double d=10.5;
	     d++;
	     System.out.println(d);
	     boolean b=true;
	  //   b++; can not apply to boolean
	     System.out.println(b);
	     
	}
}
